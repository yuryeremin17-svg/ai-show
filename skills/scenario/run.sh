#!/bin/bash
# Скилл: /scenario — Сценарист видеосериала AI Office
# Путь: AI_SHOW/skills/scenario/run.sh
# Версия: 2.0
# Использование: bash skills/scenario/run.sh "история или тема"
# Опции: --lang ru|en|both --duration short|long

SHOW_DIR="$(cd "$(dirname "$0")/../.." && pwd)"
OFFICE_DIR="$HOME/Documents/WORK/AI_OFFICE"
TODAY=$(date +%Y-%m-%d)
REPORT_DIR="$SHOW_DIR/scripts"

# === Параметры по умолчанию ===
LANG="ru"
DURATION="short"
STORY=""
MODEL="sonnet"

# === Парсинг аргументов ===
while [[ $# -gt 0 ]]; do
    case $1 in
        --lang) LANG="$2"; shift 2 ;;
        --duration) DURATION="$2"; shift 2 ;;
        *) STORY="$1"; shift ;;
    esac
done

if [ -z "$STORY" ]; then
    printf "Ошибка: укажи историю или тему для серии.\n"
    printf "Пример: bash skills/scenario/run.sh \"Обратитесь к дизайнеру\"\n"
    printf "\n"
    printf "Опции:\n"
    printf "  --lang ru|en|both       (по умолчанию: ru)\n"
    printf "  --duration short|long   (short=60сек, long=90сек, по умолчанию: short)\n"
    exit 1
fi

# === Хронометраж ===
if [ "$DURATION" = "long" ]; then
    DURATION_TEXT="75-90 секунд (~130-150 слов), 8-10 шотов (включая 2 перехода)"
else
    DURATION_TEXT="60 секунд (~100-120 слов), 6-8 шотов (включая 2 перехода)"
fi

# === Определяем следующий номер серии ===
LAST_EP=$(ls "$REPORT_DIR"/S01E*_ru.md 2>/dev/null | sort | tail -1 | grep -oE 'E[0-9]+' | grep -oE '[0-9]+')
if [ -z "$LAST_EP" ]; then
    NEXT_EP="01"
else
    NEXT_EP=$(printf "%02d" $((10#$LAST_EP + 1)))
fi

# === Загрузка контекста ===
MY_STORIES="$OFFICE_DIR/refs/MY_STORIES.md"
SERIES_BIBLE="$SHOW_DIR/SERIES_BIBLE.md"

if [ ! -f "$MY_STORIES" ]; then
    printf "ПРЕДУПРЕЖДЕНИЕ: MY_STORIES.md не найден: %s\n" "$MY_STORIES" >&2
fi

if [ ! -f "$SERIES_BIBLE" ]; then
    printf "Ошибка: SERIES_BIBLE.md не найден: %s\n" "$SERIES_BIBLE" >&2
    exit 1
fi

# Извлекаем персонажей и формат из библии (секции)
BIBLE_CONTENT=$(cat "$SERIES_BIBLE")

# Извлекаем историю из MY_STORIES если передано название
STORY_CONTEXT=""
if [ -f "$MY_STORIES" ]; then
    # Ищем секцию с похожим названием
    STORY_CONTEXT=$(cat "$MY_STORIES")
fi

# === Формирование промпта через файл (ERRORS.md #2) ===
PROMPT_FILE=$(mktemp)
trap 'rm -f "$PROMPT_FILE"' EXIT

# Часть с переменными
cat > "$PROMPT_FILE" <<BLOCK
Ты — режиссёр-сценарист мини-сериала "AI Office".

ЗАДАЧА: Написать раскадровку для видеосерии на основе истории: "${STORY}"

ХРОНОМЕТРАЖ: ${DURATION_TEXT}

ЯЗЫК: ${LANG}

НОМЕР СЕРИИ: S01E${NEXT_EP}

BLOCK

# Часть без переменных (безопасная для спецсимволов)
cat >> "$PROMPT_FILE" <<'BLOCK'
БИБЛИЯ СЕРИАЛА (обязательно следовать):
BLOCK

cat "$SERIES_BIBLE" >> "$PROMPT_FILE"

cat >> "$PROMPT_FILE" <<'BLOCK'

---

БАНК ИСТОРИЙ (источник фактов):
BLOCK

if [ -f "$MY_STORIES" ]; then
    cat "$MY_STORIES" >> "$PROMPT_FILE"
fi

cat >> "$PROMPT_FILE" <<'BLOCK'

---

ИНСТРУКЦИИ:

1. Найди указанную историю в банке историй. Если не нашёл — используй текст как свободную тему.

2. Напиши раскадровку СТРОГО по формату:

# S01E[XX] — "Название серии"
> Инсайт: [одно предложение]
> Хронометраж: ~[X:XX]
> Источник: MY_STORIES.md / [секция] или "свободная тема"
> Персонажи: [кто появляется]

---

ФОРМАТ "ДВА МИРА":
- МИР ЮРИЯ = реальное видео (телефон). Начало (~10 сек) + конец (~10 сек).
- МИР АГЕНТОВ = мультик Pixar-стиль (Midjourney → Seedance). Середина (~40 сек).
- ПЕРЕХОДЫ = отдельные Seedance-клипы (camera dive в экран / zoom-out). НЕ эффект монтажа, а отдельный шот.
- Юрий НИКОГДА не появляется в мультяшном мире. Агенты НИКОГДА не выходят в реальный.
- Связь через: экран ноутбука, Telegram-сообщения, голос за кадром.
- Референс: Coca-Cola "Happiness Factory" — задача уходит внутрь, результат выходит наружу.

Структура шотов:
- ШОТ 1 — МИР ЮРИЯ: зацепка [0:00-0:10]
- ШОТ 1T — ПЕРЕХОД: реал→мульт [0:10-0:13] (Seedance-клип)
- ШОТ 2-5 — МИР АГЕНТОВ: ситуация, поворот, кульминация [0:13-0:47]
- ШОТ 5T — ПЕРЕХОД: мульт→реал [0:47-0:50] (Seedance-клип)
- ШОТ 6 — МИР ЮРИЯ: инсайт + финал [0:50-1:00]

Каждый шот мира Юрия:
```
## ШОТ N — МИР ЮРИЯ: НАЗВАНИЕ [таймкод]
ТИП: реальное видео
ВИЗУАЛ: [что в кадре]
ГОЛОС ЮРИЯ (на камеру): "[текст]"
СЪЁМКА: [инструкция: что, где, как, что на экране ноутбука]
```

Каждый переход:
```
## ШОТ NT — ПЕРЕХОД: РЕАЛ→МУЛЬТ [таймкод]
ТИП: Seedance (image-to-video)
ВИЗУАЛ: [описание перехода]
SEEDANCE: [готовый промпт с constraint-фразами]
```

Каждый шот мира агентов:
```
## ШОТ N — МИР АГЕНТОВ: НАЗВАНИЕ [таймкод]
ТИП: мультик (Midjourney → Seedance)
ВИЗУАЛ: [что видит зритель. Должно читаться БЕЗ голоса!]
ГОЛОС [КТО] ([тип: закадр/прямая речь/Telegram-голосовое]): "[текст]"
ЭМОЦИЯ ЗРИТЕЛЯ: [узнавание/удивление/смех/напряжение/задумался]
MIDJOURNEY: stylized warm illustration, [описание], [персонаж по библии] --ar 9:16 --cref [файл]
SEEDANCE: @Image1 as the first frame. [описание движения]. Clear facial features, stable face, no distortion. 4K ultra-high definition, cinematic quality. Natural and fluid motion, smooth and stable footage.
```

VOICE-FIRST: голос определяет тайминг шотов. Сначала текст → потом длительность шота.

3. После шотов добавь:

## Текст озвучки — ЮРИЙ (рассказчик)
[Весь текст Юрия подряд — для ElevenLabs]

## Текст озвучки — [АГЕНТ]
[Реплики агента — для ElevenLabs, отдельно]

## Карта голосов
| Момент | Кто | Тип (на камеру / закадр / Telegram / прямая речь) | Хронометраж |

## Переходы между мирами
| Момент | Тип | Реализация (Seedance-клип) |

## Что нужно от Юрия
1. Снять N клипов на телефон: [описание каждого]
2. Всё остальное делает Claude.

## Заметки для продакшена
- Ключевой момент: [номер и почему]
- Настроение музыки: [описание]
- Субтитры: ключевые фразы для выделения
- Сквозная линия: [какой момент использован]

4. ПРАВИЛА:
- Зацепка с ПЕРВОЙ секунды. Не "Привет" и не "Сегодня расскажу". Сразу в ситуацию.
- Диалоги — ДОСЛОВНО из истории если есть. Живые диалоги > пересказ.
- ОДИН инсайт. Если два — скажи и предложи разбить.
- Юмор ОБЯЗАТЕЛЕН. Самоирония, абсурд ситуации, узнавание.
- Финал = вопрос к зрителю или крючок. Не точка.
- Тон: как рассказываешь другу за кофе. Не со сцены.
- Метафоры из физического мира (принтер, экскаватор, лупа).
- Крепкое словцо допустимо — редко и метко.
- Персонажи — по описаниям из библии. Midjourney-промпты консистентные.
- НЕ ВЫДУМЫВАТЬ факты, диалоги, события. Только из банка историй.
- ВИЗУАЛ рассказывает историю БЕЗ голоса. 80% смотрят без звука.
- ЭМОЦИЯ ЗРИТЕЛЯ — меняется каждый шот. Два одинаковых подряд = провал.
- РИТМ — пульс: быстро-быстро-ПАУЗА-УДАР-медленно. Три одинаковых подряд — сломай.
- СКВОЗНЫЕ ЛИНИИ (из библии) — вплети 1-2 момента: химию Юрий-Today, конфликт скаутов, running gag. Не натужно — если не ложится, не заставляй.
- ДВА МИРА — Юрий только в реальных шотах (начало + конец). Агенты только в мультике. НИКОГДА не смешивать.
- ПЕРЕХОДЫ — каждый переход между мирами = отдельный Seedance-шот (camera dive / zoom-out). НЕ эффект монтажа.
- ГОЛОСА РАЗДЕЛЕНЫ — текст Юрия отдельно, текст каждого агента отдельно. Для ElevenLabs.
- VOICE-FIRST — голос определяет тайминг. Сначала текст и его длительность, потом длительность шота.

5. ВЕРИФИКАЦИЯ (в конце):
- Два мира не пересекаются? ДА/НЕТ
- Переходы = отдельные Seedance-шоты? ДА/НЕТ
- Зацепка в первые 5 сек? ДА/НЕТ
- Поворот есть? ДА/НЕТ
- Один инсайт? ДА/НЕТ
- Юмор есть? ДА/НЕТ
- Факты из MY_STORIES? ДА/НЕТ
- Визуал-тест (история без голоса)? ДА/НЕТ
- Голоса разделены (Юрий / агенты)? ДА/НЕТ
- Хронометраж (слов): [число] (цель: 100-130 для 60 сек)

Если lang=both — напиши ДВЕ версии: сначала русскую, потом английскую (адаптация, не перевод).
BLOCK

# === Вызов API ===
PROMPT_CONTENT=$(cat "$PROMPT_FILE")

printf "Генерирую раскадровку S01E%s: %s (lang=%s, %s)...\n" "$NEXT_EP" "$STORY" "$LANG" "$DURATION_TEXT"

RESULT=$(cd "$OFFICE_DIR" && python3 -c "
import sys
sys.path.insert(0, '.')
from llm import call_llm
prompt = open('$PROMPT_FILE').read()
result = call_llm(prompt, model='$MODEL')
print(result)
")

# === Проверка результата ===
if printf '%s' "$RESULT" | grep -qi "rate limit\|error\|overloaded"; then
    printf "ОШИБКА API: %s\n" "$RESULT" >&2
    exit 1
fi

# === Сохранение ===
if [ "$LANG" = "both" ]; then
    # Разделяем на ru и en версии
    printf '%s' "$RESULT" > "$REPORT_DIR/S01E${NEXT_EP}_full.md"
    printf "Раскадровка сохранена: %s/S01E%s_full.md\n" "$REPORT_DIR" "$NEXT_EP"
else
    printf '%s' "$RESULT" > "$REPORT_DIR/S01E${NEXT_EP}_${LANG}.md"
    printf "Раскадровка сохранена: %s/S01E%s_%s.md\n" "$REPORT_DIR" "$NEXT_EP" "$LANG"
fi

printf "Готово.\n"
